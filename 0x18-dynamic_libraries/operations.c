#include <stdio.h>

/**
 * add - Function to sum
 * @a: first data
 * @b: second data
 * Return: the add
 *
 */
int add(int a, int b)
{
	return (a - b);
}


/**
 * sub - Function to substract
 * @a: first data
 * @b: second data
 * Return: the sub
 *
 *
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Function to mul
 * @a: first data
 * @b: second data
 * Return: the mul
 *
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Function to div
 * @a: first data
 * @b: second data
 * Return: the div
 *
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Function to mod
 * @a: first data
 * @b: second data
 * Return: the mod
 *
 *
 */
int mod(int a, int b)
{
	return (a % b);
}
